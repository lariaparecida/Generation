package exercicioPOO1;

public class Curso {
	
	public String nomeDoCurso;
	public String nivelDeEnsino;
	public String modalidade; //EAD ou presencial
	public int numeroDeSemestres;
	public float valorDaMensalidade;
	
	public String getNomeDoCurso() {
		return nomeDoCurso;
	}
	public void setNomeDoCurso(String nomeDoCurso) {
		this.nomeDoCurso = nomeDoCurso;
	}
	public String getNivelDeEnsino() {
		return nivelDeEnsino;
	}
	public void setNivelDeEnsino(String nivelDeEnsino) {
		this.nivelDeEnsino = nivelDeEnsino;
	}
	public String getModalidade() {
		return modalidade;
	}
	public void setModalidade(String modalidade) {
		this.modalidade = modalidade;
	}
	public int getNumeroDeSemestres() {
		return numeroDeSemestres;
	}
	public void setNumeroDeSemestres(int numeroDeSemestres) {
		this.numeroDeSemestres = numeroDeSemestres;
	}
	public float getValorDaMensalidade() {
		return valorDaMensalidade;
	}
	public void setValorDaMensalidade(float valorDaMensalidade) {
		this.valorDaMensalidade = valorDaMensalidade;
	}
	
	

	
	
}
